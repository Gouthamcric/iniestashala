      <p><b>Step-3:<br> Educational qualifications</b>- your educational degrees along with your institution, your aggregate
        percentage, and the (expected) year of graduation.
        Always follow the reverse chronological order, i.e. the most recent ones first.
      A brief table including your academic degrees and division should be sufficient to reflect your accolades.</p>
      <p><b>Step-4:<br> Skills</b>- State only similar skills your employer is looking for but use different words rather than
        choosing the same ones mentioned in the advertisement or internship post.When you are applying for an engineering internship, your technical skills are tantamount to your role.
        You can categorise your skills and use bullets to list them. </p>
        <p><b>Step-5:</b>
          <br><b>projects And Training</b> – As a college student, you work on various projects as a part of your course. These projects on your resume depict your conceptual knowledge and practical skills. Include the name of your project,
        a brief description of what it does and how it works, and mention your role in the project explicitly.
        Herein, you enlist all the certified trainings that you have taken up so far. These add credibility to your skills and signal the recruiter that you’re qualified enough to take up the job. If you haven’t done a
        substantial number of trainings, you can merge it with the ‘Work Experience’ section and name it ‘Internships and Trainings’.</p>


      <p><b>Step-6:</b><br><b>Experiance</b>- With the details of your previous internships, this section depicts your learning curve and displays how you’ve grown as a candidate.
