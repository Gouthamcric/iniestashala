       </p>

       <p><b>Step-7: <br>Additional details </b>– Your achievements and extra-curricular activities and acts as a saviour when you are taking your first step towards your career. Listing out the details of participation in various college events and competitions denote your holistic growth as a student and give the recruiter an insight into your talents
         and capabilities and a glimpse of your soft skills – confidence, emotional intelligence, teamwork, and dedication.</p>

