         underneath it or use the same font for all the details.
         A few points to keep in mind while adding these details – </p>
      <p><b>Step-2:<br> Career Objective </b>-While the career statement forms an integral part of the resume, it shouldn’t be
        too long and unfocused.
        it could grab the attention of an employer to your application in the first 2 seconds.
        Include the skills you have that are required for the internship, your interest in the field, and what you can bring to
        the table.</p>
        <h4>Points to Remember:</h4>
  <p>    <ul>
        <li>Keep it concise.    </li>
        <li>Focus on your intern role rather than life goals.</li>
        <li>Focus on a bilateral goal and not just self serving.</li>
</ul>
</p>
